package com.example.astra.model;

public class UserResponse {

	private String Content;

	public UserResponse() {
		super();
	}

	public UserResponse(String content) {
		super();
		Content = content;
	}

	public String getContent() {
		return Content;
	}

	public void setContent(String content) {
		Content = content;
	}
	
	
}
